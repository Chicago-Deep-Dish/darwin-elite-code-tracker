export default function RecordTable() {
  const [page, setPage] = useState(0);
  const [rowsPerPage, setRowsPerPage] = useState(10);

  // Avoid a layout jump when reaching the last page with empty rows.
  const emptyRows =
    page > 0 ? Math.max(0, (1 + page) * rowsPerPage - sample.length) : 0;

  const handleChangePage = (event, newPage) => {
    setPage(newPage);
  };

  const handleChangeRowsPerPage = (event) => {
    setRowsPerPage(parseInt(event.target.value, 10));
    setPage(0);
  };

  return (
    <TableContainer sx={{ maxWidth: '80%', mt: 5, mb: 5 }} component={Paper}>
      <Table sx={{ minWidth: 500 }} aria-label="custom pagination table">
        <TableHead>
          <TableRow>
            <TableCell>Prompt Name</TableCell>
            <TableCell>Prompt Link</TableCell>
            <TableCell align="right">Difficulty</TableCell>
            <TableCell align="right">Time to Complete</TableCell>
          </TableRow>
        </TableHead>
        <TableBody>
          {(rowsPerPage > 0
            ? sample.slice(page * rowsPerPage, page * rowsPerPage + rowsPerPage)
            : sample
          ).map((row) => (
            <TableRow key={row['Prompt Name']}>
              <TableCell style={{ width: 160 }}>
                {row['Prompt Name']}
              </TableCell>
              <TableCell style={{ width: 160 }}>
                <a href={row['Prompt Link']} className="prompt-link">
                  {row['Prompt Link']}
                </a>
              </TableCell>
              <TableCell style={{ width: 160 }} align="right">
                {row.Difficulty}
              </TableCell>
              <TableCell style={{ width: 160 }} align="right">
                {row['Total Time']}
              </TableCell>
            </TableRow>
          ))}

          {emptyRows > 0 && (
            <TableRow style={{ height: 53 * emptyRows }}>
              <TableCell colSpan={4} />
            </TableRow>
          )}
        </TableBody>
        <TableFooter>
          <TableRow>
            <TablePagination
              rowsPerPageOptions={[5, 10, 25, { label: 'All', value: -1 }]}
              colSpan={4}
              count={sample.length}
              rowsPerPage={rowsPerPage}
              page={page}
              SelectProps={{
                inputProps: {
                  'aria-label': 'rows per page',
                },
                native: true,
              }}
              onPageChange={handleChangePage}
              onRowsPerPageChange={handleChangeRowsPerPage}
              ActionsComponent={TablePaginationActions}
            />
          </TableRow>
        </TableFooter>
      </Table>
    </TableContainer>
  );
}
